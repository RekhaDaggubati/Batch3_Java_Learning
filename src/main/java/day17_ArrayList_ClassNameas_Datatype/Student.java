package day17_ArrayList_ClassNameas_Datatype;

public class Student
{
    //instance variables
    int stuId;
    String stuName;
    String group;
    String grade;
    //creating constructor
    public Student(int stuId, String stuName, String group, String grade)
    {
        this.stuId = stuId;
        this.stuName = stuName;
        this.group = group;
        this.grade = grade;
    }
}
