package day7_Switch_Abstract;

public class SwitchConcepts
{
    public static void main(String[] args)
    {

        //Using Switch Statement
        int no =15;
        switch(no)
        {
            case 15:
                System.out.println("No is 15");
                //break;
            case 25:
                System.out.println("No is 25");
                break;
            case 35:
                System.out.println("No is 35");
               // break;
            case 45:
                System.out.println("No is 45");
                break;
            case 55:
                System.out.println("No is 55");
                break;
            default:
                System.out.println("enter no 15 or 25 or 35 or 45 or 55");

        }

    }
}
