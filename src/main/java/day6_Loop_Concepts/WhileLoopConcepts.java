package day6_Loop_Concepts;

public class WhileLoopConcepts
{
    public static void main(String[] args)
         //Using While loop to print 1 to 10
    {
        int i=1;
        while(i <= 10)
        {
            //System.out.println(i);
            System.out.print(i + " ");
            i++;
        }
    }
}
