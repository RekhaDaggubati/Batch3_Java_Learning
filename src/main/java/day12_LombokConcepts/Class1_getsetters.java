package day12_LombokConcepts;

public class Class1_getsetters
{
    private int a;
    private int b;

//using getters and setters
    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
